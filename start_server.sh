#!/bin/bash
# Скрипт запуска FastAPI сервера для доступа из интернета

# Порт для веб-сервера (можно изменить)
PORT=${PORT:-8000}

# Запуск uvicorn на всех интерфейсах (0.0.0.0) для доступа из интернета
# --host 0.0.0.0 - слушать на всех сетевых интерфейсах
# --port $PORT - порт для доступа
# --reload - автоматическая перезагрузка при изменении кода (убрать в production)
uvicorn src.main:app --host 0.0.0.0 --port $PORT --reload

